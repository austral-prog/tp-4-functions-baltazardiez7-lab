def max_of_two(x, y):
    """Given x and y, that are 2 numbers, return the biggest number."""
    answer2=max (x,y)
    return answer2


def max_of_three(x, y, z):
    """Given x, y and z, that are 3 numbers, return the biggest number of the three."""
    answer3=max(x,y,z)
    return answer3
